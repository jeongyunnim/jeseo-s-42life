#include "ft_printf.h"

void	ft_strupper(char **ret)
{
	int	i;

	i = 0;
	while ((*ret)[i])
	{
		if ('a' <= (*ret)[i] && (*ret)[i] <= 'z')
			(*ret)[i] -= 32;
		i++;
	}
}
